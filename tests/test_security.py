import unittest
import html
from sql_compare import generate_report
import tempfile
import os

class TestSecurity(unittest.TestCase):
    def test_xss_in_html_report(self):
        xss_payload = "<script>alert('xss')</script>"
        result = {
            "ws_a": "a", "ws_b": "b", "ws_equal": False,
            "diff_ws": "", "diff_norm": "", "diff_can": "",
            "exact_equal": False, "canonical_equal": False,
            "norm_a": "a", "norm_b": "b",
            "can_a": "a", "can_b": "b",
            "summary": [xss_payload]
        }
        with tempfile.NamedTemporaryFile(suffix=".html", delete=False) as f:
            temp_path = f.name

        try:
            generate_report(result, "both", "html", temp_path, True)
            with open(temp_path, "r") as f:
                content = f.read()
            self.assertNotIn(xss_payload, content)
            self.assertIn(html.escape(xss_payload), content)
        finally:
            os.remove(temp_path)

